    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        

             
       try{
           name1=username.getText().trim();
             pass1=password.getText().trim();
             
             String name2="",mob="",add="",gen="",br="",doc="",pass="";
       
            Class.forName("com.mysql.jdbc.Driver");
         Connection con=DriverManager.getConnection("jdbc:mysql:///student","root","");
         PreparedStatement p=con.prepareStatement("select * from student_table where `name`='"+name1+"' && `password`='"+pass1+"'");
  
           ResultSet rs=p.executeQuery();
           while(rs.next())
           {
               name2=rs.getString("name");
               mob=rs.getString("mobile_no");
              add =rs.getString("adress");
              gen=rs.getString("gender");
              br=rs.getString("branch");
              doc=rs.getString("documents");
              pass=rs.getString("password");
               

           }
           if(add.equals("") && gen.equals(""))
           {
               JOptionPane.showMessageDialog(Login.this,"login unsuccessful");
               username.setText(" ");
           password.setText("");
               
           }
           else{
               JOptionPane.showMessageDialog(Login.this,"login successful");
               username.setText(" ");
           password.setText("");
            Student_profile s=new Student_profile(name2,mob,add,gen,br,doc,pass);
               s.setVisible(true);
           
           }
           

      
           
         p.close();
         con.close();
     }
     catch(Exception e1)
     {
         System.out.println(e1);
     }
                           

    }//GEN-LAST:event_loginActionPerformed

